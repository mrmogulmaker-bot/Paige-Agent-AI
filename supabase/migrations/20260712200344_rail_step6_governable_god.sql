CREATE OR REPLACE FUNCTION public.can_access_rail_topic(_topic text)
RETURNS boolean
LANGUAGE plpgsql STABLE SECURITY DEFINER SET search_path = public AS $$
DECLARE
  v_tenant uuid := public.current_user_tenant_id();
  v_uid uuid := auth.uid();
  v_contact_id uuid;
  v_ct_tenant uuid;
BEGIN
  IF v_uid IS NULL OR _topic IS NULL THEN RETURN false; END IF;
  IF v_tenant IS NOT NULL AND _topic = 'rail:tenant:' || v_tenant::text THEN
    RETURN public.is_platform_owner() OR public.has_any_role(v_uid, ARRAY['admin','super_admin','coach']);
  END IF;
  IF _topic LIKE 'rail:client:%' THEN
    BEGIN
      v_contact_id := substring(_topic FROM 13)::uuid;
    EXCEPTION WHEN others THEN RETURN false;
    END;
    SELECT c.tenant_id INTO v_ct_tenant FROM public.clients c WHERE c.id = v_contact_id;
    IF v_ct_tenant IS NULL THEN RETURN false; END IF;
    RETURN EXISTS (SELECT 1 FROM public.clients c WHERE c.id = v_contact_id AND c.linked_user_id = v_uid)
        OR public.is_platform_owner()
        OR (v_ct_tenant = v_tenant AND public.has_any_role(v_uid, ARRAY['admin','super_admin','coach']));
  END IF;
  IF _topic = 'rail:platform' THEN RETURN public.is_platform_owner(); END IF;
  RETURN false;
END $$;
