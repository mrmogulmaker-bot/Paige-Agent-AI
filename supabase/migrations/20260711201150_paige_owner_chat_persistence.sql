-- Multi-chat persistence for "Your Paige" (owner workspace). Reuses paige_chat_threads
-- + paige_chat_turns (already have RLS + the create/append RPCs). Adds only: a monotonic
-- order key, a rolling-summary watermark, an owner-list index, a sliding retention window,
-- and a privacy tightening. No new tables, no RLS re-enable.

-- 1. Monotonic per-turn ordinal (deterministic ordering + summary/tail boundary math).
ALTER TABLE public.paige_chat_turns ADD COLUMN IF NOT EXISTS seq bigserial;
CREATE INDEX IF NOT EXISTS idx_paige_chat_turns_thread_seq
  ON public.paige_chat_turns (thread_id, seq);

-- 2. Rolling-summary watermark: the summary covers turns with seq <= this value, so the
--    summarized range and the verbatim tail can never overlap.
