-- Move the shared pg_cron trigger token out of plaintext (cron.job command +
-- edge source) into Vault. The value is GENERATED server-side and never leaves
-- the database: cron reads it from Vault to build the header (cron_token_header),
-- and the edge function verifies the received header against Vault
-- (verify_cron_token). The token therefore appears in no source file, no
-- cron.job command, and no chat transcript.

-- Fresh random token, provisioned into Vault only if not already present.
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM vault.secrets WHERE name = 'cron_token') THEN
    PERFORM vault.create_secret(
      encode(gen_random_bytes(24), 'hex'),
      'cron_token',
      'Shared bearer that authorizes pg_cron -> edge-function trigger calls (process-booking-notifications, plan-reminder-cron).'
    );
  END IF;
END $$;

-- The edge functions (service role) call this to authorize a cron trigger.
CREATE OR REPLACE FUNCTION public.verify_cron_token(_token text)
RETURNS boolean LANGUAGE sql SECURITY DEFINER SET search_path = public, vault AS $$
  SELECT _token IS NOT NULL
     AND _token = (SELECT decrypted_secret FROM vault.decrypted_secrets WHERE name = 'cron_token');
$$;
